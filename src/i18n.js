function interpolate(template, params) {
    return String(template).replace(/\{(\w+)\}/g, (match, key) => (params && params[key] != null) ? String(params[key]) : '');
}

// 取指定语言的文案；缺失时回退到中文，再回退到 key 本身。
function t(lang, key, params) {
    const table = STRINGS[normalizeLang(lang)] || STRINGS[DEFAULT_LANG];
    const raw = (table && table[key] != null) ? table[key] : (STRINGS[DEFAULT_LANG][key] != null ? STRINGS[DEFAULT_LANG][key] : key);
    return interpolate(raw, params);
}

// 将 VS Code 显示语言（如 en / zh-cn / zh-tw / ja）映射到受支持的语言：
// 以 zh 开头的（zh-cn / zh-tw 等）归为简体中文，其余归为英文（当前仅支持 zh / en）。
function matchVscodeLang(lang) {
    return String(lang || '').toLowerCase().startsWith('zh') ? 'zh' : 'en';
}

module.exports = { STRINGS, t, interpolate, DEFAULT_LANG, SUPPORTED_LANGS, normalizeLang, matchVscodeLang };
